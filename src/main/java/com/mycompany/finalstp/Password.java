    public void errorMessages() {

        int rnd = (int) (Math.random() * 4);

        ArrayList<String> ar = new ArrayList<>();
        ar.add("Password is incorrect. Please try again.");
        ar.add("Access denied: Wrong password provided.");
        ar.add("Login unsuccessful: Password incorrect.");
        ar.add("Error: Password mismatch.");

        String error = ar.toString().split(",")[rnd].replace("[", "").replace("]", "");
        jLabel3.setForeground(Color.red);
        jLabel3.setText(error);
        jLabel3.setVisible(true);

    }

    public void PatientPass() {
        try {

            File path = new File(patientPassLoc + jLabel1.getText().toString() + ".txt");
            String fileName = path.toString();

            BufferedReader b = new BufferedReader(new FileReader(fileName));
            String s;

            ArrayList<String> files = new ArrayList<>();

            while ((s = b.readLine()) != null) {
                files.add(s.replace(",", "|").replace("[", ""));
            }
            System.out.println(s);
            b.close();
            String c = files.toString();

            String patientPW = c.split(",")[22].replace("Password:", "").replace(" ", "").replace("]", "");
            setPpass(patientPW);

        } catch (Exception e) {

        }

    }

