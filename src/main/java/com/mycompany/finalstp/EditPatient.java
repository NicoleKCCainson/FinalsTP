    public void dep() {
        doc.removeAllItems();
        File path = new File(fileLocDoctor);
        File[] files = path.listFiles();
        ArrayList<String> fileArray = new ArrayList<>();

        for (int i = 0; i < files.length;) {

            if (files[i].isFile()) {
                try {
                    BufferedReader br = new BufferedReader(new FileReader(files[i]));
                    String s;
                    Arrays.sort(files);

                    while ((s = br.readLine()) != null) {
                        if (s.trim().contains(depart.getSelectedItem().toString())) {
                            int num = 0;
                            String n = files[i].getName().toString();
                            fileArray.add(n);
                            String aa = n.split(",")[num].replace(".txt", "");
                            doc.addItem(aa);
                            num++;
                            System.out.println(n);
                        }

                    }
                    br.close();

                } catch (Exception e) {

                }
            }
            i++;
        } if(depart.getSelectedItem().toString() == "NO DEPARTMENT SELECTED"){
          allDoc();
        }
    }
 
